#include <stdio.h> // Inclui a biblioteca padrão de entrada e saída (standard input/output)

// A função 'main' é o ponto de entrada de todo programa C
// O programa sempre começa a execução aqui.
int main()
{
    // printf() é uma função que imprime texto na tela (console)
    printf("Olá, mundo!\n"); // "\n" é um caractere de nova linha, faz o cursor ir para a próxima linha

    return 0; // Retorna 0 para indicar que o programa terminou com sucesso
}